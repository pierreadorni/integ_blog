function onHomeHover(element){
    element.children[0].setAttribute('src','src/home_black.png');
    element.children[1].style.color = 'black';
}
function onHomeUnHover(element){
    element.children[0].setAttribute('src','src/home.png');
    element.children[1].style.color = 'white';
}

